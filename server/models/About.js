const mongoose = require('mongoose');

const Schema = mongoose.Schema;
const AboutSchema = new Schema({
 
  body: {
    type: String,
    required: true
  },
  createdAt: {
    type: Date,
    default: Date.now
  },
  updatedAt: {
    type: Date,
    default: Date.now
  }
});

module.exports = mongoose.model('About', AboutSchema);